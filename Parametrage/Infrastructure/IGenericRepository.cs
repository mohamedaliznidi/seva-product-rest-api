using Microsoft.EntityFrameworkCore.Query;
using System.Linq.Expressions;

namespace Parametrage.Infrastructure
{
    public interface IGenericRepository<T> where T : class
    {
        IEnumerable<T> GetAll();
        T GetById(object id);
        void Insert(T obj);
        void Update(T obj);
        void Delete(object id);
        void Save();
    }
}
